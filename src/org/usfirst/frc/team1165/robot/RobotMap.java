package org.usfirst.frc.team1165.robot;
 
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap
{
	public static final int pickupWheelPistonsInChannel		= 0;
	public static final int pickupWheelPistonsOutChannel	= 1;

	public static final int toteLifterPistonHighIn			= 2;
	public static final int toteLifterPistonHighOut			= 3;	
}